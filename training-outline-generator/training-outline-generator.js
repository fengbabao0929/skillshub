const fs = require('fs');

// 颜色输出
const colors = {
  reset: '\x1b[0m',
  bright: '\x1b[1m',
  cyan: '\x1b[36m',
  green: '\x1b[32m',
  yellow: '\x1b[33m',
  blue: '\x1b[34m',
  magenta: '\x1b[35m'
};

function colorize(text, color) {
  return `${colors[color]}${text}${colors.reset}`;
}

// 生成课程标题选项
function generateCourseTitles(topic) {
  const templates = [
    `${topic}实战培训（1-2天）`,
    `${topic}专业技能提升课程`,
    `${topic}从入门到精通（实战班）`,
    `${topic}实务操作与案例分析`,
    `${topic}系统化培训课程`
  ];
  return templates.map((t, i) => `${i + 1}. ${t}`);
}

// 教学方式库
