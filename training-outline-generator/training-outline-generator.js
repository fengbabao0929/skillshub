// 主函数
function main() {
  const args = process.argv.slice(2);
  const topic = args[0];
  const options = {
    days: 2,
    save: args.includes('--save')
  };

  // 解析天数参数
  const daysIndex = args.indexOf('--days');
  if (daysIndex !== -1 && args[daysIndex + 1]) {
    options.days = parseInt(args[daysIndex + 1]) || 2;
  }

  if (!topic) {
    console.log('');
    console.log(colorize('╔══════════════════════════════════════╗', 'cyan'));
    console.log(colorize('║   专业技能培训课程大纲生成器        ║', 'cyan'));
    console.log(colorize('╚══════════════════════════════════════╝', 'cyan'));
    console.log('');
    console.log(colorize('用法: node training-outline-generator.js <课程主题> [选项]', 'yellow'));
    console.log('');
    console.log('选项:');
    console.log('  --days <1|2>      课程天数（默认2天）');
    console.log('  --save           保存到文件');
    console.log('');
    console.log('示例:');
    console.log('  node training-outline-generator.js "Python数据分析" --days 1');
    console.log('  node training-outline-generator.js "财务报表分析" --save');
    console.log('');
    process.exit(1);
  }

  console.log('');
  console.log(colorize('╔══════════════════════════════════════╗', 'cyan'));
  console.log(colorize('║   专业技能培训课程大纲生成器        ║', 'cyan'));
  console.log(colorize('╚══════════════════════════════════════╝', 'cyan'));
  console.log('');
  console.log(colorize(`📋 课程主题: ${topic}`, 'bright'));
  console.log(colorize(`⏱️  课程时长: ${options.days}天`, 'bright'));
  console.log('');

  const outline = options.days === 1
    ? generateOneDayOutline(topic)
    : generateTwoDayOutline(topic);

  console.log(outline);

  // 保存到文件
  if (options.save) {
    const safeTopic = topic.replace(/[<>:"/\\|?*]/g, '_');
    const filename = `training_outline_${safeTopic}_${options.days}days_${Date.now()}.md`;
    fs.writeFileSync(filename, outline, 'utf-8');
    console.log('');
    console.log(colorize(`✓ 已保存到: ${filename}`, 'green'));
    console.log('');
  }
}

main();
